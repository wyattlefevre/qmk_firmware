    static const char* FAST_TYPE_FRAMES [] = {ANIM_FRAME1, ANIM_FRAME2, ANIM_FRAME5};
    static const char* SLOW_TYPE_FRAMES [] = {ANIM_FRAME5, ANIM_FRAME4, ANIM_FRAME3};

    static char wpm_str[4];
    void oled_render_speed(void) {
        sprintf(wpm_str, "%03d", get_current_wpm());
        oled_set_cursor(0, 1);
        oled_write_P(PSTR("WPM"), false);
        oled_set_cursor(0, 2);
        oled_write(wpm_str, false);
    }


    static uint32_t anim_timer = 0;
    static uint8_t current_frame = 0;

    const uint8_t speed = get_current_wpm();

    if (timer_elapsed32(anim_timer) > ANIM_FRAME) {
        anim_timer = timer_read32();
        const char* frame = speed > FAST_TYPE_WPM ? FAST_TYPE_FRAMES[current_frame] : SLOW_TYPE_FRAMES[current_frame];
        oled_write_raw_P(frame, ANIM_SIZE);
        current_frame = (current_frame + 1) % 3;
        oled_render_speed();
    }
